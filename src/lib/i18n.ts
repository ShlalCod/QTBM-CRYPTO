const translations: Record<string, Translations> = { en, ar };

function getNestedValue(obj: Translations, keyPath: string): string | undefined {
  const keys = keyPath.split('.');
  let current: TranslationValue | undefined = obj;
  
  for (const key of keys) {
    if (current && typeof current === 'object' && key in current) {
      current = current[key];
    } else {
      return undefined;
    }
  }
  
  return typeof current === 'string' ? current : undefined;
}

export function t(key: string, lang?: string): string {
  const language = lang || useAppStore.getState().language;
  const translation = getNestedValue(translations[language], key);
  if (translation) return translation;
  
  // Fallback to English
  if (language !== 'en') {
    const fallback = getNestedValue(translations.en, key);
    if (fallback) return fallback;
  }
  
  // Return the key itself as last resort
  return key;
}

export function useTranslation() {
  const language = useAppStore((state) => state.language);
  
  const translate = (key: string): string => {
    const translation = getNestedValue(translations[language], key);
    if (translation) return translation;
    
    // Fallback to English
    if (language !== 'en') {
      const fallback = getNestedValue(translations.en, key);
      if (fallback) return fallback;
    }
    
    return key;
  };
  
  return { t: translate, language, isRTL: language === 'ar' };
}

